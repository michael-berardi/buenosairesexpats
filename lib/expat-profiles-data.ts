export type ExpatRegion = 
  | "north-america" 
  | "europe" 
  | "asia" 
  | "middle-east" 
  | "latin-america" 
  | "oceania";

export type ExpatStatus = "active" | "inactive" | "former";

export interface ExpatProfile {
  id: string;
  name: string;
  nationality: string;
  region: ExpatRegion;
  blogUrl: string;
  blogName: string;
  yearsInBA: string;
  status: ExpatStatus;
  bio: string;
  tags: string[];
  neighborhood?: string;
  profession?: string;
  socialLinks?: {
    instagram?: string;
    twitter?: string;
    youtube?: string;
  };
  featured?: boolean;
  seoDescription: string;
  hasScreenshot: boolean;
}

// Helper functions
export function getExpatById(id: string): ExpatProfile | undefined {
  return expatProfiles.find(profile => profile.id === id);
}

export function getExpatsByRegion(region: ExpatRegion): ExpatProfile[] {
  return expatProfiles.filter(profile => profile.region === region);
}

export function getExpatsByTag(tag: string): ExpatProfile[] {
  return expatProfiles.filter(profile => 
    profile.tags.some(t => t.toLowerCase() === tag.toLowerCase())
  );
}

export function getFeaturedExpats(): ExpatProfile[] {
  return expatProfiles.filter(profile => profile.featured);
}

export function getVerifiedExpats(): ExpatProfile[] {
  return expatProfiles.filter(profile => profile.hasScreenshot);
}

