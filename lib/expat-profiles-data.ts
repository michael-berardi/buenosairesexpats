export function getRegionLabel(region: ExpatRegion): string {
  const labels: Record<ExpatRegion, string> = {
    "north-america": "North America",
    "europe": "Europe",
    "asia": "Asia",
    "middle-east": "Middle East",
    "latin-america": "Latin America",
    "oceania": "Oceania",
  };
  return labels[region];
}

